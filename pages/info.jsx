function PageInfo({ navigate, type }) {
  const page = INFO_PAGES[type] || INFO_PAGES.faq;
  return (
    <main>
      <PageHero
        eyebrow={page.eyebrow}
        title={page.title}
        lead={page.lead}
        crumbs={[page.title.replace("。", "")]}
        navigate={navigate}
      />
      <section style={{ background: "var(--paper)" }}>
        <div className="container narrow">
          {page.links ? (
            <div className="sitemap-grid">
              {page.links.map(([route, label]) => (
                <button key={route} className="sitemap-link" onClick={() => navigate(route)}>
                  <span>{label}</span>
                  <span className="arrow" />
                </button>
              ))}
            </div>
          ) : page.fullText ? (
            <article className="full-text-document">
              {page.fullText.map((paragraph, i) => {
                const isMainTitle = i === 0;
                const isCenter = paragraph === "記";
                const isEnd = paragraph === "以上";
                const isMajor = paragraph.startsWith("○");
                const isSub = paragraph.startsWith("【");
                const isNote = paragraph.startsWith("※");
                const className = [
                  isMainTitle ? "doc-title-line" : "",
                  isCenter ? "doc-center-line" : "",
                  isEnd ? "doc-end-line" : "",
                  isMajor ? "doc-major-line" : "",
                  isSub ? "doc-sub-line" : "",
                  isNote ? "doc-note-line" : "",
                  !isMainTitle && !isCenter && !isEnd && !isMajor && !isSub && !isNote ? "doc-body-line" : "",
                ].filter(Boolean).join(" ");
                return (
                  <p key={`${i}-${paragraph.slice(0, 12)}`} className={className}>
                    {paragraph}
                  </p>
                );
              })}
            </article>
          ) : (
            <div className="info-card-list">
              {page.body.map((item, i) => (
                <article key={item.heading} className="info-card">
                  <div className="info-num font-serif-en">{String(i + 1).padStart(2, "0")}</div>
                  <div>
                    <h2 className="font-serif-jp">{item.heading}</h2>
                    <p>{item.text}</p>
                  </div>
                </article>
              ))}
            </div>
          )}
          <div className="article-cta">
            <div>
              <div className="eyebrow">Contact</div>
              <h3 className="font-serif-jp">不明点があれば、メールからご相談ください。</h3>
            </div>
            <button className="btn btn-primary" onClick={() => navigate("contact")}>無料相談へ <span className="arrow" /></button>
          </div>
        </div>
      </section>
    </main>
  );
}

window.PageInfo = PageInfo;
